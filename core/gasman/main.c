U0 render(U8 page, U8 ind) {
  printf("\033[H\033[K\033[1;37mGASMAN 1.2 | PAGE %02X (%d) | INDEX %02X (%03d)\033[0m\n", page, page, ind, ind);
  printf("     ");
  for (I16 i = 0; i < LINESIZE; i++) {
    printf("%02X    ", i);
  }
  for (I16 i = 0; i < PAGESIZE; i++) {
    if (i % LINESIZE == 0) {
      printf("\n%02X | ", i);
    }
    if (i == ind) {
      printf("\033[47m\033[30m%s\033[0m ", GC_TABLE[page][i]);
    }
    else {
      printf("%s ", GC_TABLE[page][i]);
    }
  }
}

